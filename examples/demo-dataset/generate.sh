#!/usr/bin/env bash
#
# Generates a realistic, longitudinal single-patient demo corpus for the
# MedMe app (patient: 张建国, 男, chronic hypertension + T2DM + CVD/stroke
# history, 2023-2026). Covers every supported document type and every
# supported file modality (.txt, text-layer .pdf via `cupsfilter`, and
# scanned-photo-look .png/.jpg via Pillow).
#
# Output: examples/demo-dataset/corpus/  (NOT imported into any vault here)
#
# Requirements: macOS `cupsfilter` (for PDF) and `python3` + Pillow (for
# scan images). Both are verified before use.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CORPUS_DIR="$SCRIPT_DIR/corpus"
RENDER_SCAN="$SCRIPT_DIR/render_scan.py"

mkdir -p "$CORPUS_DIR"

echo "== MedMe demo dataset generator =="
echo "Corpus dir: $CORPUS_DIR"

# ---------------------------------------------------------------------------
# Preflight checks
# ---------------------------------------------------------------------------
if ! command -v cupsfilter >/dev/null 2>&1; then
  echo "ERROR: cupsfilter not found (required for PDF generation on macOS)." >&2
  exit 1
fi
if ! python3 -c "import PIL" >/dev/null 2>&1; then
  echo "ERROR: python3 Pillow (PIL) not found (required for scan-image generation)." >&2
  exit 1
fi

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

# write_txt <relative-filename>   (content piped in via stdin)
write_txt() {
  local out="$CORPUS_DIR/$1"
  cat > "$out"
  echo "  [txt] $1 ($(wc -c < "$out" | tr -d ' ') bytes)"
}

# write_pdf <relative-filename>   (plain-text content piped in via stdin,
# rendered to a text-layer PDF via cupsfilter)
write_pdf() {
  local out="$CORPUS_DIR/$1"
  local tmp
  tmp="$(mktemp "${TMPDIR:-/tmp}/medme_src.XXXXXX.txt")"
  cat > "$tmp"
  cupsfilter "$tmp" > "$out" 2>/dev/null
  rm -f "$tmp"
  local size
  size="$(wc -c < "$out" | tr -d ' ')"
  if [[ "$size" -eq 0 ]]; then
    echo "ERROR: generated PDF is empty: $1" >&2
    exit 1
  fi
  if ! file "$out" | grep -q "PDF document"; then
    echo "ERROR: generated file is not a valid PDF: $1" >&2
    exit 1
  fi
  echo "  [pdf] $1 ($size bytes)"
}

# write_scan <relative-filename> [--handwriting]   (plain-text content piped
# in via stdin, rendered to a realistic scanned/photographed report image)
write_scan() {
  local out="$CORPUS_DIR/$1"
  local flag="${2:-}"
  local tmp
  tmp="$(mktemp "${TMPDIR:-/tmp}/medme_src.XXXXXX.txt")"
  cat > "$tmp"
  python3 "$RENDER_SCAN" "$out" $flag < "$tmp"
  rm -f "$tmp"
  local size
  size="$(wc -c < "$out" | tr -d ' ')"
  if [[ "$size" -eq 0 ]]; then
    echo "ERROR: generated scan image is empty: $1" >&2
    exit 1
  fi
  python3 -c "from PIL import Image; im = Image.open('$out'); im.verify()" \
    || { echo "ERROR: generated image failed to verify: $1" >&2; exit 1; }
  echo "  [img] $1 ($size bytes)"
}

echo ""
echo "-- Generating records --"

